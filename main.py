# Функция для ввода граммовок
def ввод_граммовок():
    мясо = float(input("Введите количество грамм мяса: "))
    лук = float(input("Введите количество грамм лука: "))
    помидор = float(input("Введите количество грамм помидоров: "))
    масло = float(input("Введите количество грамм масла: "))
    специи = float(input("Введите количество грамм специй: "))
    return мясо, лук, помидор, масло, специи

# Функция для написания полного способа приготовления
def написание_рецепта(мясо, лук, помидор, масло, специи):
    рецепт = f"""
    Рецепт приготовления шашлыка:

    Ингредиенты:
    - Мясо: {мясо} г
    - Лук: {лук} г
    - Помидоры: {помидор} г
    - Масло: {масло} г
    - Специи: {специи} г

    Шаги приготовления:
    1. Нарежьте мясо, лук и помидоры на кусочки.
    2. Приготовьте маринад, смешав масло и специи.
    3. Замаринуйте мясо, лук и помидоры в подготовленном маринаде.
    4. Нанизывайте мясо, лук и помидоры на шпажки.
    5. Обжаривайте шашлык на гриле или мангале до готовности.

    Приятного аппетита!
    """
    return рецепт

# Функция для записи в текстовый файл
def запись_в_файл(рецепт):
    with open("рецепт_шашлыка.txt", "w", encoding="utf-8") as файл:
        файл.write(рецепт)
    print("Рецепт успешно записан в файл.")

# Основной код
мясо, лук, помидор, масло, специи = ввод_граммовок()
рецепт = написание_рецепта(мясо, лук, помидор, масло, специи)
запись_в_файл(рецепт)
